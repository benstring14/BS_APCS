public class SimpleCounterButAForLoop
{
    public static void main(String[] args)
    {
       
        for(int foo = 0; foo <= 300; foo++)
        
        {
            System.out.println("The value is: " +foo);

        }
        for(int poo = 300; poo >= 0; poo--)
        
        {
            System.out.println("The value is: " +poo);

        }
        for(int koo = 4798; koo <= 5324; koo++)
        
        {
            System.out.println("The value is: " +koo);

        }
        for(int loo = 5324; loo >= 4798; loo--)
        
        {
            System.out.println("The value is: " +loo);

        }
    }
}