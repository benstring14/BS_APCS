public class JavaWordPattern
{
    public static void main(String[] args)
    {
        //I used 1, 2, 3 respectively for each letter and filled the spaced with 0s, then used VS Code to replace each number with a letter and each 0 with a space to create "Java Word Pattern"
        System.out.println("    J    A   V     V   A");
        System.out.println("    J   A A   V   V   A A");
        System.out.println("J   J  AAAAA   V V   AAAAA");
        System.out.println(" J J  A     A   V   A     A");
    }
}